import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap transition-colors disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg:not([class*='size-'])]:size-4 shrink-0 [&_svg]:shrink-0 outline-none focus-visible:ring-ring/50 focus-visible:ring-[3px] aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive",
  {
    variants: {
      variant: {
        default:
          "border-2 border-accent text-accent text-sm font-bold uppercase tracking-widest hover:bg-accent hover:text-bg-primary",
        secondary:
          "border-2 border-text-primary bg-bg-primary text-text-primary text-sm font-bold uppercase tracking-widest hover:bg-text-primary hover:text-bg-primary hover:border-bg-primary",
        outline:
          "border-2 border-white text-white text-sm font-bold uppercase tracking-widest hover:bg-white hover:text-black",
        underline: "text-sm font-medium text-text-secondary hover:underline",
      },
      size: {
        default: "px-6 py-2 has-[>svg]:px-4",
        sm: "h-8 rounded-md gap-1.5 px-3 has-[>svg]:px-2.5",
        lg: "h-10 rounded-md px-6 has-[>svg]:px-4",
        icon: "size-9",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

function Button({
  className,
  variant,
  size,
  asChild = false,
  ...props
}: React.ComponentProps<"button"> &
  VariantProps<typeof buttonVariants> & {
    asChild?: boolean;
  }) {
  const Comp = asChild ? Slot : "button";

  return (
    <Comp
      data-slot="button"
      className={cn(buttonVariants({ variant, size, className }))}
      {...props}
    />
  );
}
export type ButtonVariantProps = VariantProps<typeof buttonVariants>;

export { Button, buttonVariants };
